package com.catclaw.aura.data.moment.local

import androidx.room.Entity
import androidx.room.PrimaryKey
import com.catclaw.aura.domain.model.MomentCard

@Entity(tableName = "moment_cards")
data class MomentCardEntity(
    @PrimaryKey val id: String,
    val createdAtEpochMs: Long,
    val posterPath: String?,
    val videoPath: String?,
    val audioPath: String?,
    val videoDurationMs: Long,
    val audioDurationMs: Long,
    val musicTitle: String?,
    val musicArtist: String?,
    val musicAlbum: String?,
    val musicActive: Boolean,
    val musicStatusMessage: String,
    val musicPackageName: String?,
    val latitude: Double?,
    val longitude: Double?,
    val locationAccuracyMeters: Float?,
    val locationProvider: String?,
    val locationPlaceName: String?,
    val sceneDescription: String?,
    val sceneDescriptionError: String?,
    val captureErrorSummary: String?,
    val themeColor: String?,
    val thumbnailUrl: String?,
    val videoUrl: String?,
    val audioUrl: String?,
    val serverStatus: String?,
)

fun MomentCardEntity.toDomain(): MomentCard = MomentCard(
    id = id,
    createdAtEpochMs = createdAtEpochMs,
    posterPath = posterPath,
    videoPath = videoPath,
    audioPath = audioPath,
    videoDurationMs = videoDurationMs,
    audioDurationMs = audioDurationMs,
    musicTitle = musicTitle,
    musicArtist = musicArtist,
    musicAlbum = musicAlbum,
    musicActive = musicActive,
    musicStatusMessage = musicStatusMessage,
    musicPackageName = musicPackageName,
    latitude = latitude,
    longitude = longitude,
    locationAccuracyMeters = locationAccuracyMeters,
    locationProvider = locationProvider,
    locationPlaceName = locationPlaceName,
    sceneDescription = sceneDescription,
    sceneDescriptionError = sceneDescriptionError,
    captureErrorSummary = captureErrorSummary,
    themeColor = themeColor,
    thumbnailUrl = thumbnailUrl,
    videoUrl = videoUrl,
    audioUrl = audioUrl,
    serverStatus = serverStatus,
)

fun MomentCard.toEntity(): MomentCardEntity = MomentCardEntity(
    id = id,
    createdAtEpochMs = createdAtEpochMs,
    posterPath = posterPath,
    videoPath = videoPath,
    audioPath = audioPath,
    videoDurationMs = videoDurationMs,
    audioDurationMs = audioDurationMs,
    musicTitle = musicTitle,
    musicArtist = musicArtist,
    musicAlbum = musicAlbum,
    musicActive = musicActive,
    musicStatusMessage = musicStatusMessage,
    musicPackageName = musicPackageName,
    latitude = latitude,
    longitude = longitude,
    locationAccuracyMeters = locationAccuracyMeters,
    locationProvider = locationProvider,
    locationPlaceName = locationPlaceName,
    sceneDescription = sceneDescription,
    sceneDescriptionError = sceneDescriptionError,
    captureErrorSummary = captureErrorSummary,
    themeColor = themeColor,
    thumbnailUrl = thumbnailUrl,
